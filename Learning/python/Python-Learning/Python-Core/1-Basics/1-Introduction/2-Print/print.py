# Basic print function
print("Hi!, Python")
print("Hello!, Python")
print("--------------------------")
print("       Learn Python       ")
print("--------------------------")
print("           __")
print("          / _)")
print("   .-^^^-/ /  ")
print("__/       /   ")
print("<__.|_|-|_|   ")

# Escape Sequences (\n, \t, \b, \', \", \\)
print("Hello\nWorld")  # New Line
print("Hello\tWorld")  # Tab
print("Hello\bWorld")  # Backspace
print("Hello'World")  # Single Quote (Alternate way to write print("Hello'World"))
print('Hello"World')  # Double Quote (Alternate way to write print('Hello"World'))
print("Hello\\World")  # Backslash
print(r"Hello\World")  # Raw String

print('Hi!, "Python"')
print('Hi!, "Python"')  # Alternate way to write the above line
print("Hi!, 'Python'")
print("Hi!, 'Python'")  # Alternate way to write the above line

print("Path: \\usr\\bin\\python3")

print("Message1")
print()
print("Message2")

print("Message1\n\n")
print("\nMessage2")

print("Message1\n\n\nMessage2")

print("Message1", end="")  # end="" is used to print the next line in the same line
print("Message2")

print("Message1\nMessage2")

print("Message1\tMessage2")

# print challenge
print("Your Learning Path:\n\t- Python Basics\n\t- Data Engineering\n\t- AI")

